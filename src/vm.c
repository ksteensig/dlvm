#include "vm.h"

dlvm_t *dlvm_init(uint8_t *p, uint64_t ps, uint64_t ss) {
    dlvm_t *vm = malloc(sizeof(dlvm_t));

    vm->program = p;
    vm->program_size = ps;

	vm->stack = malloc(sizeof(ttype_t *) * ss);
	vm->stack_size = ss;

    vm->pc = 0;
    vm->sp = 0;
    vm->fp = 0;

    return vm;
}





































































































































































































































































































































































































































































































































